    private void btnMostrarPuntoElejidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMostrarPuntoElejidoMouseClicked
        String distritoSeleccionado = (String) cbFiltroDistrito.getSelectedItem();
        String resultado = "";
        int contador = 0;
        
        for(PuntoCritico punto : DatosCompartidos.puntosCriticos){
            if(punto.getDistrito().equalsIgnoreCase(distritoSeleccionado)){
                resultado += "Direccion: "+punto.getDireccion()+"\n";
                resultado += "Prioridad: "+punto.getPrioridad()+"\n";
                resultado += "Observacion: "+punto.getObservacion()+"\n";
                resultado += "====================================\n";
                contador++;
            }
        }
        if(resultado.isEmpty()){
            txtAreaResultados.setText("No se ecnotro el punto del Distrito seleccionado!");
            txtCantidadDistrito.setText("");
        }else{
            txtCantidadDistrito.setText("Cantidad de Puntos en este Distrito: "+ contador);
            txtAreaResultados.setText(resultado);
        }
    }//GEN-LAST:event_btnMostrarPuntoElejidoMouseClicked

