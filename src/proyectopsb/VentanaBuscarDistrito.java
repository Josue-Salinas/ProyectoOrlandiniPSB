    private void btnBuscarporPalabraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarporPalabraMouseClicked
        
        String palabraClave = txtPalabraClave.getText().trim().toLowerCase();
        String encontrado = "";
        
        if(palabraClave.isEmpty()){
            JOptionPane.showMessageDialog(this, "Escriba una palabra para Buscar!!!");
            return;
        }
        for(PuntoCritico punto : DatosCompartidos.puntosCriticos){
            if(punto.getObservacion().toLowerCase().contains(palabraClave)){
                encontrado += "Direccion: "+punto.getDireccion()+"\n";
                encontrado += "Distrito: "+punto.getDistrito()+"\n";
                encontrado += "Prioridad: "+punto.getPrioridad()+"\n";
                encontrado += "Observacion: "+punto.getObservacion()+"\n";
                encontrado += "==============================\n";
            }
        }
        if(encontrado.isEmpty()){
            txtAreaResultados.setText("No se ecnotro el punto del Distrito seleccionado!");
        }else{
            txtAreaResultados.setText(encontrado);
        }
    }//GEN-LAST:event_btnBuscarporPalabraMouseClicked

    private boolean contieneDistrito(javax.swing.JComboBox<String> combo, String distrito){
        for(int i = 0; i < combo.getItemCount(); i++){
            if(combo.getItemAt(i).equals(distrito)){
                return true;
            }
        }
        return false;
    }
