    private void btnEliminarUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarUsuarioMouseClicked
        
        List<String> usuSelect = jlistUsuariosRegistrados.getSelectedValuesList();
        
        if(usuSelect == null){
            JOptionPane.showMessageDialog(this, "Seleccione un usuario para eliminarlo!!");
            return;
        }
        
       int confirm = JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar al usuario seleccionado?", "confirmar", JOptionPane.YES_NO_OPTION);
       if (confirm != JOptionPane.YES_OPTION) return;

       for (String a : usuSelect) {
           String correo = a.split(" - ")[1].trim();
           if (!correo.equals("admin@admin.com")) {
               DatosCompartidos.usuarios.remove(correo);
           }
       }
       actualizarLista();
       JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente");
    }//GEN-LAST:event_btnEliminarUsuarioMouseClicked

