    private void actualizarLista(){
        DefaultListModel<String> lista = new DefaultListModel<>();
        for(Map.Entry<String, Usuario> usu : DatosCompartidos.usuarios.entrySet()){
            String correo = usu.getKey();
            Usuario usuario = usu.getValue();
            if(!correo.equalsIgnoreCase("admin@admin.com")){
                lista.addElement(usuario.getNombre()+" - "+correo);
            }
        }
        jlistUsuariosRegistrados.setModel(lista);
    }

