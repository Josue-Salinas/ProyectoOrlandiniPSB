    private void cbDistritoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbDistritoBuscarActionPerformed

    cbDireccionBuscar.removeAllItems();
    String distritoSeleccionado = (String) cbDistritoBuscar.getSelectedItem();

    for (PuntoCritico p : DatosCompartidos.puntosCriticos) {
        if (p.getDistrito().equals(distritoSeleccionado)) {
            cbDireccionBuscar.addItem(p.getDireccion());
        }
    }
        
    }//GEN-LAST:event_cbDistritoBuscarActionPerformed

