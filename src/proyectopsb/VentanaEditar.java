    private void btnGuardarCambiosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarCambiosMouseClicked

    String distritoSeleccionado = (String) cbDistritoBuscar.getSelectedItem();
    String direccionSeleccionada = (String) cbDireccionBuscar.getSelectedItem();

    String nuevaDireccion = txtNuevaDireccion.getText().trim();
    String nuevoDistrito = txtNuevoDistrito.getText().trim();
    String nuevaPrioridad = (String) cbNuevaPrioridad.getSelectedItem();
    String nuevaObservacion = txtAreaObservacion.getText().trim();
    String estadoSeleccionado = (String) cbEstado.getSelectedItem();

    if (nuevaObservacion.length() > 200) {
    JOptionPane.showMessageDialog(this, "La observación no puede tener más de 200 caracteres.");
    return;
    }
    if (nuevaDireccion.isEmpty() || nuevoDistrito.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Complete todos los campos");
        return;
    }
    
    if(estadoSeleccionado.equals("Solucionado")){
        nuevoDistrito += " (Solucionado)";
    }else{
        nuevoDistrito += " *";
    }

    for (PuntoCritico pb : DatosCompartidos.puntosCriticos) {
        if (pb.getDistrito().equals(distritoSeleccionado) && pb.getDireccion().equals(direccionSeleccionada)) {
            int respuesta = JOptionPane.showConfirmDialog(this, "Esta seguro de guardar los cambios?","confirma", JOptionPane.YES_NO_OPTION);
            if(respuesta != JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(this,"Cambios Caneclados");
                return;
            }
            
            pb.setDireccion(nuevaDireccion);
            pb.setDistrito(nuevoDistrito);
            pb.setPrioridad(nuevaPrioridad);
            pb.setObservacion(nuevaObservacion);
            
            JOptionPane.showMessageDialog(this, "Punto actualizado correctamente!");
            
            txtNuevaDireccion.setText("");
            txtNuevoDistrito.setText("");
            txtAreaObservacion.setText("");
            cbNuevaPrioridad.setSelectedIndex(0);
            cbEstado.setSelectedIndex(0);
            return;
        }
    }

    JOptionPane.showMessageDialog(this, "No se encontro el punnto para actualizar");
        
    }//GEN-LAST:event_btnGuardarCambiosMouseClicked

